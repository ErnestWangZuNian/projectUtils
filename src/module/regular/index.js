let regular = {
  // email地址
  email: /^\w+([-+.]\w+)*@\w+([-.]\w+)*\.\w+([-.]\w+)*$/,
  //  国内手机号码
  phone: /^1[34578]{1}\d{9}$/,
  // 帐号是否合法(字母开头，允许5-16字节，允许字母数字下划线)：^[a-zA-Z]\w{4,15}$
  account: /^[a-zA-Z]\w{4,15}$/,
  //  密码(以字母开头，长度在6~18之间，只能包含字母、数字和下划线)
  password: /^[a-zA-Z].{5,17}$/,
  //  身份证正则，15位数或者18位,  
  idCard: /(^\d{15}$)|(^\d{17}([0-9]|X)$)/,
  //  银行卡正则,16到19的正则表达式
  bankCard: /^\d{16,19}$/
}
export default regular;